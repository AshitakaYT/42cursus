#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	if (!*alst)
		*alst = new;
	else if ((*alst)->next)
		ft_lstadd_back(&(*alst)->next, new);
	else
		(*alst)->next = new;
}
